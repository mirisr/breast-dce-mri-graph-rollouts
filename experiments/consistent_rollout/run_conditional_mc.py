@dataclass(frozen=True)
class MeanRolloutRecord:
    patient_id: str
    fold: int
    start_idx: int
    pred_idx: int
    start_visit: str
    predicted_visit: str
    rollout_depth: int
    n_sv: int
    pred_pos: np.ndarray
    obs_pos: np.ndarray
    x_pred: np.ndarray
    x_obs: np.ndarray
    alive_prob: np.ndarray
    alive_obs: np.ndarray
    pred_ftv_ml: float
    obs_ftv_ml: float
    ftv_t0_ml: float
    collection: str | None = None
    pCR: int | None = None
    subtype: str | None = None


@dataclass(frozen=True)
class ResidualRecord:
    patient_id: str
    fold: int
    start_idx: int
    pred_idx: int
    ftv_resid: float
    alive_count_resid: float
    global_disp_resid: np.ndarray
    local_disp_resids: np.ndarray
    log_volume_resids: np.ndarray


def _safe_float(v: Any) -> float:
    try:
        x = float(v)
        return x if math.isfinite(x) else float("nan")
    except Exception:
        return float("nan")


def _clip01(p: np.ndarray) -> np.ndarray:
    return np.clip(np.asarray(p, dtype=np.float64), 1e-6, 1.0 - 1e-6)


def sample_alive_mask_gumbel_topk(
    alive_prob: np.ndarray,
    alive_count: int,
    rng: np.random.Generator,
) -> np.ndarray:
    """Sample exactly ``alive_count`` alive nodes using Gumbel-top-k logits."""
    p = _clip01(alive_prob).reshape(-1)
    n = int(p.shape[0])
    k = int(np.clip(round(float(alive_count)), 0, n))
    mask = np.zeros(n, dtype=bool)
    if k <= 0:
        return mask
    if k >= n:
        mask[:] = True
        return mask
    logits = np.log(p) - np.log1p(-p)
    scores = logits + rng.gumbel(size=n)
    idx = np.argpartition(scores, -k)[-k:]
    mask[idx] = True
    return mask


def conformal_correction(scores: Iterable[float], alpha: float = 0.10) -> float:
    """Finite-sample split-conformal correction using the higher empirical rank."""
    arr = np.asarray([x for x in scores if np.isfinite(x)], dtype=np.float64)
    if arr.size == 0:
        return 0.0
    arr.sort()
    rank = int(math.ceil((arr.size + 1) * (1.0 - float(alpha))))
    rank = min(max(rank, 1), arr.size)
    return float(arr[rank - 1])


def _bucket_key(start_idx: int, pred_idx: int) -> tuple[int, int]:
    return int(start_idx), int(pred_idx)


def _experiment_checkpoint(runs_dir: Path, fold: int, checkpoint_name: str) -> Path:
    return runs_dir / f"fold{int(fold)}" / checkpoint_name


def _load_models_by_fold(runs_dir: Path, checkpoint_name: str) -> dict[int, tuple[Any, torch.Tensor, torch.Tensor]]:
    models: dict[int, tuple[Any, torch.Tensor, torch.Tensor]] = {}
    for fold in range(5):
        ckpt = _experiment_checkpoint(runs_dir, fold, checkpoint_name)
        if not ckpt.is_file():
            raise FileNotFoundError(f"Missing consistent rollout checkpoint: {ckpt}")
        models[fold] = lib.load_model(ckpt)
    return models


def _spatial_knn_edges_device(pos: torch.Tensor, k: int = 8) -> torch.Tensor:
    d = torch.cdist(pos, pos)
    d.fill_diagonal_(float("inf"))
    k_eff = min(k, pos.shape[0] - 1)
    if k_eff <= 0:
        return torch.zeros((2, 0), dtype=torch.long, device=pos.device)
    _, idx = torch.topk(d, k=k_eff, largest=False, dim=1)
    src = torch.arange(pos.shape[0], device=pos.device).unsqueeze(1).expand_as(idx).reshape(-1)
    dst = idx.reshape(-1)
    ei = torch.stack([torch.cat([src, dst]), torch.cat([dst, src])], dim=0)
    return ei.unique(dim=1)


def _build_history_edges_device(history_pos: list[torch.Tensor], k_spatial: int = 8) -> torch.Tensor:
    all_src: list[torch.Tensor] = []
    all_dst: list[torch.Tensor] = []
    offsets: list[int] = []
    cur = 0
    for pos in history_pos:
        offsets.append(cur)
        cur += int(pos.shape[0])
    offsets.append(cur)

    for v, pos in enumerate(history_pos):
        off = offsets[v]
        n_v = int(pos.shape[0])
        if n_v > 1:
            ei_v = _spatial_knn_edges_device(pos, k=k_spatial)
            if ei_v.numel():
                all_src.append(ei_v[0] + off)
                all_dst.append(ei_v[1] + off)
        if v < len(history_pos) - 1:
            n_next = int(history_pos[v + 1].shape[0])
            n_link = min(n_v, n_next)
            if n_link > 0:
                src_t = torch.arange(n_link, device=pos.device) + off
                dst_t = torch.arange(n_link, device=pos.device) + offsets[v + 1]
                all_src += [src_t, dst_t]
                all_dst += [dst_t, src_t]
    if not all_src:
        return torch.zeros((2, 0), dtype=torch.long, device=history_pos[0].device)
    return torch.stack([torch.cat(all_src), torch.cat(all_dst)], dim=0).long()


@torch.no_grad()
def rollout_from_visit_device(
    model: Any,
    mean: torch.Tensor,
    std: torch.Tensor,
    g: dict,
    *,
    start_visit: int,
    device: torch.device,
    k_spatial: int = 8,
) -> list[dict[str, Any]]:
    """Device-aware version of ``consistent_twin_lib.rollout_from_visit``."""
    if start_visit < 0 or start_visit >= len(VISITS) - 1:
        raise ValueError(f"start_visit must be in [0, {len(VISITS) - 2}]")

    model = model.to(device)
    mean_d = mean.to(device)
    std_d = std.to(device)
    x = (g["x"].float().to(device) - mean_d) / std_d
    pos = g["pos"].float().to(device)
    off = [int(v) for v in g["visit_offsets"].tolist()]
    n_visits = len(off) - 1

    history_x: list[torch.Tensor] = []
    history_pos: list[torch.Tensor] = []
    history_t: list[torch.Tensor] = []
    for v in range(start_visit + 1):
        sl = slice(off[v], off[v + 1])
        xv = x[sl].clone()
        pv = pos[sl].clone()
        tv = torch.full((xv.shape[0],), float(v), dtype=x.dtype, device=device)
        history_x.append(xv)
        history_pos.append(pv)
        history_t.append(tv)

    rows: list[dict[str, Any]] = []
    for pred_v in range(start_visit + 1, n_visits):
        x_cat = torch.cat(history_x, dim=0)
        pos_cat = torch.cat(history_pos, dim=0)
        t_cat = torch.cat(history_t, dim=0)
        edge_index = _build_history_edges_device(history_pos, k_spatial=k_spatial)
        out = model(x_cat, pos_cat, t_cat, edge_index)

        n_last = int(history_x[-1].shape[0])
        sl_last = slice(x_cat.shape[0] - n_last, x_cat.shape[0])
        dpos = out["delta_pos"][sl_last]
        dfeat = out["delta_feat"][sl_last]
        alive_prob = torch.sigmoid(out["alive_logit"][sl_last])

        x_next = history_x[-1] + dfeat
        pos_next = history_pos[-1] + dpos

        obs_sl = slice(off[pred_v], off[pred_v + 1])
        x_obs = g["x"][obs_sl].float().cpu().numpy()
        pos_obs = g["pos"][obs_sl].float().cpu().numpy()
        alive_obs = g["alive"][obs_sl].detach().cpu().numpy().astype(bool)

        rows.append(
            {
                "visit_idx": pred_v,
                "visit_name": VISITS[pred_v],
                "pos_pred": pos_next.detach().cpu().numpy(),
                "x_pred": (x_next * std_d + mean_d).detach().cpu().numpy(),
                "alive_prob": alive_prob.detach().cpu().numpy(),
                "pos_obs": pos_obs,
                "x_obs": x_obs,
                "alive_obs": alive_obs,
            }
        )

        history_x.append(x_next)
        history_pos.append(pos_next)
        history_t.append(torch.full((x_next.shape[0],), float(pred_v), dtype=x.dtype, device=device))
    return rows


def _cohort_metadata(cohort: pd.DataFrame) -> dict[str, dict[str, Any]]:
    rows: dict[str, dict[str, Any]] = {}
    for _, r in cohort.iterrows():
        pid = str(r["patient_id"])
        rows[pid] = {
            "collection": str(r["collection"]) if pd.notna(r.get("collection", None)) else None,
            "pCR": int(r["pCR"]) if pd.notna(r.get("pCR", None)) else None,
            "subtype": str(r["subtype"]) if pd.notna(r.get("subtype", None)) else None,
        }
    return rows


def _select_patient_folds(
    folds: pd.DataFrame,
    graphs_root: Path,
    patient_list: Path | None,
    patient_limit: int,
) -> list[tuple[str, int]]:
    fold_map = {str(r.patient_id): int(r.fold) for r in folds.itertuples(index=False)}
    if patient_list is not None and patient_list.is_file():
        pids = [ln.strip() for ln in patient_list.read_text().splitlines() if ln.strip()]
    else:
        pids = folds["patient_id"].astype(str).tolist()
    out = [(pid, fold_map[pid]) for pid in pids if pid in fold_map and (graphs_root / f"{pid}.pt").is_file()]
    if patient_limit > 0:
        out = out[: int(patient_limit)]
    return out


def _observed_ftv_t0(g: dict, volume_idx: int) -> float:
    off = g["visit_offsets"].tolist()
    sl = slice(int(off[0]), int(off[1]))
    return _safe_float(g["x"][sl, volume_idx].detach().cpu().numpy().sum())


def _load_consistent_graph(pid: str, graphs_root: Path) -> dict:
    path = graphs_root / f"{pid}.pt"
    if not path.is_file():
        raise FileNotFoundError(path)
    return torch.load(path, map_location="cpu", weights_only=False)


def build_mean_rollout_records(
    *,
    patient_folds: list[tuple[str, int]],
    models_by_fold: dict[int, tuple[Any, torch.Tensor, torch.Tensor]],
    graphs_root: Path,
    cohort_meta: dict[str, dict[str, Any]],
    start_visits: tuple[int, ...],
    volume_idx: int,
    device: torch.device,
) -> list[MeanRolloutRecord]:
    records: list[MeanRolloutRecord] = []
    for i, (pid, fold) in enumerate(patient_folds, 1):
        model, mean, std = models_by_fold[int(fold)]
        g = _load_consistent_graph(pid, graphs_root)
        ftv_t0 = _observed_ftv_t0(g, volume_idx)
        meta = cohort_meta.get(pid, {})
        for start_idx in start_visits:
            try:
                steps = rollout_from_visit_device(
                    model, mean, std, g, start_visit=int(start_idx), device=device
                )
            except Exception as exc:
                print(f"[warn] {pid} fold={fold} start={start_idx}: rollout failed: {exc}", flush=True)
                continue
            for step_i, step in enumerate(steps, 1):
                alive_prob = np.asarray(step["alive_prob"], dtype=np.float64).reshape(-1)
                x_pred = np.asarray(step["x_pred"], dtype=np.float64)
                x_obs = np.asarray(step["x_obs"], dtype=np.float64)
                pred_vol = np.clip(x_pred[:, volume_idx], 0.0, None)
                obs_vol = np.clip(x_obs[:, volume_idx], 0.0, None)
                pred_ftv = _safe_float(np.sum(pred_vol * alive_prob))
                obs_ftv = _safe_float(np.sum(obs_vol))
                records.append(
                    MeanRolloutRecord(
                        patient_id=pid,
                        fold=int(fold),
                        start_idx=int(start_idx),
                        pred_idx=int(step["visit_idx"]),
                        start_visit=VISITS[int(start_idx)],
                        predicted_visit=str(step["visit_name"]),
                        rollout_depth=step_i,
                        n_sv=int(alive_prob.shape[0]),
                        pred_pos=np.asarray(step["pos_pred"], dtype=np.float64),
                        obs_pos=np.asarray(step["pos_obs"], dtype=np.float64),
                        x_pred=x_pred,
                        x_obs=x_obs,
                        alive_prob=alive_prob,
                        alive_obs=np.asarray(step["alive_obs"], dtype=bool).reshape(-1),
                        pred_ftv_ml=pred_ftv,
                        obs_ftv_ml=obs_ftv,
                        ftv_t0_ml=ftv_t0,
                        collection=meta.get("collection"),
                        pCR=meta.get("pCR"),
                        subtype=meta.get("subtype"),
                    )
                )
        if i % 50 == 0 or i == len(patient_folds):
            print(f"[mean] {i}/{len(patient_folds)} patients -> {len(records)} rollout records", flush=True)
    return records


def residual_from_record(r: MeanRolloutRecord, volume_idx: int) -> ResidualRecord:
    alive_obs = np.asarray(r.alive_obs, dtype=bool)
    pred_pos = np.asarray(r.pred_pos, dtype=np.float64)
    obs_pos = np.asarray(r.obs_pos, dtype=np.float64)
    valid = alive_obs & np.isfinite(pred_pos).all(axis=1) & np.isfinite(obs_pos).all(axis=1)
    if valid.any():
        disp = obs_pos[valid] - pred_pos[valid]
        global_disp = disp.mean(axis=0)
        local_disp = disp - global_disp[None, :]
    else:
        global_disp = np.zeros(3, dtype=np.float64)
        local_disp = np.empty((0, 3), dtype=np.float64)

    pred_vol = np.clip(r.x_pred[:, volume_idx], 0.0, None)
    obs_vol = np.clip(r.x_obs[:, volume_idx], 0.0, None)
    vvalid = valid & np.isfinite(pred_vol) & np.isfinite(obs_vol)
    log_volume_resids = np.log1p(obs_vol[vvalid]) - np.log1p(pred_vol[vvalid])
    return ResidualRecord(
        patient_id=r.patient_id,
        fold=r.fold,
        start_idx=r.start_idx,
        pred_idx=r.pred_idx,
        ftv_resid=_safe_float(r.obs_ftv_ml - r.pred_ftv_ml),
        alive_count_resid=_safe_float(float(alive_obs.sum()) - float(np.asarray(r.alive_prob).sum())),
        global_disp_resid=global_disp,
        local_disp_resids=local_disp,
        log_volume_resids=np.asarray(log_volume_resids, dtype=np.float64),
    )


def build_residual_buckets(
    records: list[MeanRolloutRecord],
    volume_idx: int,
) -> dict[tuple[int, int], list[ResidualRecord]]:
    buckets: dict[tuple[int, int], list[ResidualRecord]] = {}
    for r in records:
        buckets.setdefault(_bucket_key(r.start_idx, r.pred_idx), []).append(residual_from_record(r, volume_idx))
    return buckets


def candidate_residuals(
    bucket: list[ResidualRecord],
    target_patient_id: str,
) -> tuple[list[ResidualRecord], np.ndarray, np.ndarray]:
    """Return residual candidates excluding the target patient."""
    patient_resids = [r for r in bucket if r.patient_id != target_patient_id]
    local = [r.local_disp_resids for r in patient_resids if r.local_disp_resids.size]
    logv = [r.log_volume_resids.reshape(-1) for r in patient_resids if r.log_volume_resids.size]
    local_arr = np.concatenate(local, axis=0) if local else np.empty((0, 3), dtype=np.float64)
    logv_arr = np.concatenate(logv, axis=0) if logv else np.empty((0,), dtype=np.float64)
    return patient_resids, local_arr, logv_arr


def _empty_sample_metrics() -> dict[str, float]:
    return {m: float("nan") for m in PRIMARY_METRICS}


def _primary_cloud_metrics(
    pred_pos: np.ndarray,
    obs_pos: np.ndarray,
    pred_ftv: float,
    obs_ftv: float,
    pred_alive_count: int,
    obs_alive_count: int,
) -> dict[str, float]:
    if pred_pos.shape[0] == 0 or obs_pos.shape[0] == 0:
        dice = 1.0 if pred_pos.shape[0] == 0 and obs_pos.shape[0] == 0 else 0.0
        return {
            "swd_mm": float("nan"),
            "chamfer_mm": float("nan"),
            "dice": float(dice),
            "ftv_abs_err_ml": abs(float(pred_ftv) - float(obs_ftv)),
            "alive_count_abs_err": abs(int(pred_alive_count) - int(obs_alive_count)),
        }
    dice, _ = voxelized_dice_iou(pred_pos, obs_pos)
    return {
        "swd_mm": _safe_float(sliced_wasserstein(pred_pos, obs_pos)),
        "chamfer_mm": _safe_float(chamfer_position(pred_pos, obs_pos)),
        "dice": _safe_float(dice),
        "ftv_abs_err_ml": abs(float(pred_ftv) - float(obs_ftv)),
        "alive_count_abs_err": abs(int(pred_alive_count) - int(obs_alive_count)),
    }


def simulate_record(
    r: MeanRolloutRecord,
    bucket: list[ResidualRecord],
    *,
    n_mc: int,
    rng: np.random.Generator,
    eps_pcr: float,
    volume_idx: int,
    metric_draws: int = 0,
) -> tuple[list[dict[str, Any]], dict[str, Any]]:
    patient_resids, local_resids, logvol_resids = candidate_residuals(bucket, r.patient_id)
    n_candidates = len(patient_resids)
    obs_alive_count = int(np.asarray(r.alive_obs, dtype=bool).sum())
    obs_cloud = np.asarray(r.obs_pos, dtype=np.float64)[np.asarray(r.alive_obs, dtype=bool)]
    pred_vol = np.clip(np.asarray(r.x_pred[:, volume_idx], dtype=np.float64), 0.0, None)
    alive_mass = float(np.asarray(r.alive_prob, dtype=np.float64).sum())
    sample_rows: list[dict[str, Any]] = []
    ftv_samples: list[float] = []

    if n_candidates:
        chosen = rng.integers(0, n_candidates, size=int(n_mc))
    else:
        chosen = np.zeros(int(n_mc), dtype=np.int64)

    for draw in range(int(n_mc)):
        if n_candidates:
            res = patient_resids[int(chosen[draw])]
            ftv_resid = float(res.ftv_resid)
            alive_resid = float(res.alive_count_resid)
            global_resid = np.asarray(res.global_disp_resid, dtype=np.float64)
        else:
            ftv_resid = 0.0
            alive_resid = 0.0
            global_resid = np.zeros(3, dtype=np.float64)

        alive_count = int(np.clip(round(alive_mass + alive_resid), 0, r.n_sv))
        alive_mask = sample_alive_mask_gumbel_topk(r.alive_prob, alive_count, rng)

        if local_resids.shape[0]:
            local_idx = rng.integers(0, local_resids.shape[0], size=r.n_sv)
            local_noise = local_resids[local_idx]
        else:
            local_noise = np.zeros_like(r.pred_pos, dtype=np.float64)
        pos_sample = np.asarray(r.pred_pos, dtype=np.float64) + global_resid[None, :] + local_noise

        if logvol_resids.shape[0]:
            lv_idx = rng.integers(0, logvol_resids.shape[0], size=r.n_sv)
            lv_noise = logvol_resids[lv_idx]
        else:
            lv_noise = np.zeros(r.n_sv, dtype=np.float64)
        vol_sample = np.expm1(np.log1p(pred_vol) + lv_noise)
        vol_sample = np.clip(vol_sample, 0.0, None)
        ftv_node = float(vol_sample[alive_mask].sum())
        ftv_sample = max(0.0, ftv_node + ftv_resid)
        ftv_samples.append(ftv_sample)

        if metric_draws <= 0 or draw < metric_draws:
            metrics = _primary_cloud_metrics(
                pos_sample[alive_mask],
                obs_cloud,
                pred_ftv=ftv_sample,
                obs_ftv=r.obs_ftv_ml,
                pred_alive_count=int(alive_mask.sum()),
                obs_alive_count=obs_alive_count,
            )
        else:
            metrics = _empty_sample_metrics()

        sample_rows.append(
            {
                "patient_id": r.patient_id,
                "fold": r.fold,
                "start_visit": r.start_visit,
                "predicted_visit": r.predicted_visit,
                "start_idx": r.start_idx,
                "pred_idx": r.pred_idx,
                "rollout_depth": r.rollout_depth,
                "draw": draw,
                "n_calibration_patients": n_candidates,
                "ftv_sample_ml": ftv_sample,
                "alive_count_sample": int(alive_mask.sum()),
                **metrics,
            }
        )

    arr = np.asarray(ftv_samples, dtype=np.float64)
    raw_lo, raw_hi = np.percentile(arr, [5, 95]) if arr.size else (float("nan"), float("nan"))
    metric_cols = {
        f"{m}_mc_mean": _safe_float(np.nanmean([row[m] for row in sample_rows]))
        for m in PRIMARY_METRICS
    }
    summary = {
        "patient_id": r.patient_id,
        "fold": r.fold,
        "collection": r.collection,
        "pCR": r.pCR,
        "subtype": r.subtype,
        "start_visit": r.start_visit,
        "predicted_visit": r.predicted_visit,
        "start_idx": r.start_idx,
        "pred_idx": r.pred_idx,
        "rollout_depth": r.rollout_depth,
        "n_mc": int(arr.size),
        "n_supervoxels": r.n_sv,
        "n_calibration_patients": n_candidates,
        "pred_ftv_det_ml": r.pred_ftv_ml,
        "obs_ftv_ml": r.obs_ftv_ml,
        "ftv_mc_mean_ml": _safe_float(np.mean(arr)),
        "ftv_mc_std_ml": _safe_float(np.std(arr)),
        "ftv_mc_median_ml": _safe_float(np.median(arr)),
        "ftv_raw_p05_ml": _safe_float(raw_lo),
        "ftv_raw_p95_ml": _safe_float(raw_hi),
        "ftv_raw_width90_ml": _safe_float(raw_hi - raw_lo),
        "coverage90_ftv_raw": int(raw_lo <= r.obs_ftv_ml <= raw_hi) if np.isfinite(raw_lo) else 0,
        "crps_ftv": _safe_float(crps_empirical(arr, r.obs_ftv_ml)),
        "pcr_prob_mc": _safe_float(np.mean(arr < float(eps_pcr))),
        "alive_count_obs": obs_alive_count,
        "alive_mass_det": alive_mass,
        "ftv_t0_ml": r.ftv_t0_ml,
        **metric_cols,
    }
    return sample_rows, summary


def add_conformal_intervals(
    summaries: list[dict[str, Any]],
    alpha: float,
) -> list[dict[str, Any]]:
    by_bucket: dict[tuple[int, int], list[dict[str, Any]]] = {}
    for row in summaries:
        score = max(
            float(row["ftv_raw_p05_ml"]) - float(row["obs_ftv_ml"]),
            float(row["obs_ftv_ml"]) - float(row["ftv_raw_p95_ml"]),
            0.0,
        )
        row["ftv_raw_nonconformity_ml"] = _safe_float(score)
        by_bucket.setdefault(_bucket_key(row["start_idx"], row["pred_idx"]), []).append(row)

    for row in summaries:
        peers = [
            x["ftv_raw_nonconformity_ml"]
            for x in by_bucket[_bucket_key(row["start_idx"], row["pred_idx"])]
            if x["patient_id"] != row["patient_id"]
        ]
        q = conformal_correction(peers, alpha=alpha)
        lo = float(row["ftv_raw_p05_ml"]) - q
        hi = float(row["ftv_raw_p95_ml"]) + q
        row["ftv_conformal_q_ml"] = q
        row["ftv_conformal_p05_ml"] = lo
        row["ftv_conformal_p95_ml"] = hi
        row["ftv_conformal_width90_ml"] = hi - lo
        row["coverage90_ftv_conformal"] = int(lo <= float(row["obs_ftv_ml"]) <= hi)
    return summaries


def aggregate_summary(per_patient: pd.DataFrame) -> dict[str, Any]:
    group_cols = ["start_visit", "predicted_visit"]
    metric_cols = [
        "ftv_mc_mean_ml",
        "ftv_mc_std_ml",
        "ftv_raw_width90_ml",
        "ftv_conformal_width90_ml",
        "coverage90_ftv_raw",
        "coverage90_ftv_conformal",
        "crps_ftv",
        "pcr_prob_mc",
        *[f"{m}_mc_mean" for m in PRIMARY_METRICS],
    ]
    rows: list[dict[str, Any]] = []
    for keys, grp in per_patient.groupby(group_cols, dropna=False):
        start_visit, predicted_visit = keys
        out: dict[str, Any] = {
            "start_visit": start_visit,
            "predicted_visit": predicted_visit,
            "n_patients": int(grp["patient_id"].nunique()),
            "mean_rollout_depth": _safe_float(pd.to_numeric(grp["rollout_depth"], errors="coerce").mean()),
        }
        for col in metric_cols:
            vals = pd.to_numeric(grp[col], errors="coerce").dropna().to_numpy(dtype=np.float64)
            out[f"{col}_mean"] = _safe_float(np.mean(vals)) if vals.size else float("nan")
            out[f"{col}_se"] = _safe_float(np.std(vals, ddof=1) / math.sqrt(vals.size)) if vals.size > 1 else float("nan")
        rows.append(out)

    t3 = [
        r for r in rows
        if r["predicted_visit"] == "T3"
    ]
    t3 = sorted(t3, key=lambda x: VISITS.index(str(x["start_visit"])))
    shrink = []
    for r in t3:
        shrink.append(
            {
                "conditioning": f"{','.join(VISITS[:VISITS.index(str(r['start_visit'])) + 1])}->T3",
                "start_visit": r["start_visit"],
                "n_patients": r["n_patients"],
                "ftv_conformal_width90_ml_mean": r.get("ftv_conformal_width90_ml_mean"),
                "ftv_raw_width90_ml_mean": r.get("ftv_raw_width90_ml_mean"),
                "crps_ftv_mean": r.get("crps_ftv_mean"),
                "ftv_abs_err_ml_mc_mean_mean": r.get("ftv_abs_err_ml_mc_mean_mean"),
            }
        )

    return {
        "n_rows": int(len(per_patient)),
        "n_patients": int(per_patient["patient_id"].nunique() if len(per_patient) else 0),
        "by_conditioning_visit": rows,
        "t3_uncertainty_shrinkage": shrink,
    }


def calibration_summary(
    residual_buckets: dict[tuple[int, int], list[ResidualRecord]],
    per_patient: pd.DataFrame,
) -> dict[str, Any]:
    rows: list[dict[str, Any]] = []
    for (start_idx, pred_idx), bucket in sorted(residual_buckets.items()):
        local_n = int(sum(r.local_disp_resids.shape[0] for r in bucket))
        logv_n = int(sum(r.log_volume_resids.shape[0] for r in bucket))
        ftv_resids = np.asarray([r.ftv_resid for r in bucket], dtype=np.float64)
        alive_resids = np.asarray([r.alive_count_resid for r in bucket], dtype=np.float64)
        grp = per_patient[
            (per_patient["start_idx"] == start_idx)
            & (per_patient["pred_idx"] == pred_idx)
        ]
        rows.append(
            {
                "start_visit": VISITS[start_idx],
                "predicted_visit": VISITS[pred_idx],
                "n_records": int(len(bucket)),
                "n_unique_patients": int(len({r.patient_id for r in bucket})),
                "n_local_disp_residuals": local_n,
                "n_log_volume_residuals": logv_n,
                "ftv_resid_mean_ml": _safe_float(np.nanmean(ftv_resids)) if ftv_resids.size else float("nan"),
                "ftv_resid_std_ml": _safe_float(np.nanstd(ftv_resids)) if ftv_resids.size else float("nan"),
                "alive_count_resid_mean": _safe_float(np.nanmean(alive_resids)) if alive_resids.size else float("nan"),
                "alive_count_resid_std": _safe_float(np.nanstd(alive_resids)) if alive_resids.size else float("nan"),
                "raw_coverage90_ftv": _safe_float(pd.to_numeric(grp["coverage90_ftv_raw"], errors="coerce").mean()) if len(grp) else float("nan"),
                "conformal_coverage90_ftv": _safe_float(pd.to_numeric(grp["coverage90_ftv_conformal"], errors="coerce").mean()) if len(grp) else float("nan"),
                "mean_conformal_q_ml": _safe_float(pd.to_numeric(grp["ftv_conformal_q_ml"], errors="coerce").mean()) if len(grp) else float("nan"),
                "mean_crps_ftv": _safe_float(pd.to_numeric(grp["crps_ftv"], errors="coerce").mean()) if len(grp) else float("nan"),
            }
        )
    return {"residual_buckets": rows}


def parse_start_visits(vals: list[int]) -> tuple[int, ...]:
    out = tuple(sorted({int(v) for v in vals}))
    bad = [v for v in out if v < 0 or v > 2]
    if bad:
        raise ValueError(f"start visits must be in [0, 1, 2], got {bad}")
    return out


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--runs-dir", type=Path, default=Path("runs/consistent_forecaster_v2/v2_sched_samp"))
    ap.add_argument("--checkpoint-name", default="best.pt")
    ap.add_argument("--graphs-root", type=Path, default=Path("datasets/ispy2/graphs_consistent"))
    ap.add_argument("--folds", type=Path, default=Path("datasets/ispy2/folds.parquet"))
    ap.add_argument("--cohort", type=Path, default=Path("datasets/ispy2/cohort.parquet"))
    ap.add_argument("--patient-list", type=Path, default=None)
    ap.add_argument("--patient-limit", type=int, default=0)
    ap.add_argument("--start-visits", type=int, nargs="+", default=[0, 1, 2])
    ap.add_argument("--n-mc", type=int, default=256)
    ap.add_argument("--metric-draws", type=int, default=0,
                    help="Number of draws per patient/visit with cloud metrics. 0 means all draws.")
    ap.add_argument("--eps-pcr", type=float, default=0.1)
    ap.add_argument("--interval", type=float, default=0.90)
    ap.add_argument("--volume-idx", type=int, default=1)
    ap.add_argument("--seed", type=int, default=42)
    ap.add_argument("--device", default="auto", choices=("auto", "cpu", "cuda"),
                    help="Use cuda on Cradle by default when available; cpu for local smoke tests.")
    ap.add_argument("--out-dir", type=Path, default=Path("reports/conditional_mc_consistent_rollout"))
    args = ap.parse_args()

    start_visits = parse_start_visits(args.start_visits)
    if args.n_mc <= 0:
        raise ValueError("--n-mc must be positive")
    alpha = 1.0 - float(args.interval)
    if not (0.0 < alpha < 1.0):
        raise ValueError("--interval must be between 0 and 1")

    folds = pd.read_parquet(args.folds)
    cohort = pd.read_parquet(args.cohort)
    patient_folds = _select_patient_folds(folds, args.graphs_root, args.patient_list, args.patient_limit)
    if not patient_folds:
        raise RuntimeError("No evaluable patients found.")
    device_name = "cuda" if args.device == "auto" and torch.cuda.is_available() else "cpu" if args.device == "auto" else args.device
    device = torch.device(device_name)
    if args.device == "cuda" and not torch.cuda.is_available():
        raise RuntimeError("--device cuda requested but torch.cuda.is_available() is false")

    print(
        f"[conditional-mc] patients={len(patient_folds)} n_mc={args.n_mc} "
        f"runs={args.runs_dir} graphs={args.graphs_root} device={device}",
        flush=True,
    )
    models_by_fold = _load_models_by_fold(args.runs_dir, args.checkpoint_name)
    records = build_mean_rollout_records(
        patient_folds=patient_folds,
        models_by_fold=models_by_fold,
        graphs_root=args.graphs_root,
        cohort_meta=_cohort_metadata(cohort),
        start_visits=start_visits,
        volume_idx=int(args.volume_idx),
        device=device,
    )
    if not records:
        raise RuntimeError("No rollout records generated.")

    residual_buckets = build_residual_buckets(records, volume_idx=int(args.volume_idx))
    sample_rows: list[dict[str, Any]] = []
    summary_rows: list[dict[str, Any]] = []
    rng = np.random.default_rng(int(args.seed))
    for i, r in enumerate(records, 1):
        bucket = residual_buckets[_bucket_key(r.start_idx, r.pred_idx)]
        rows, summary = simulate_record(
            r,
            bucket,
            n_mc=int(args.n_mc),
            rng=rng,
            eps_pcr=float(args.eps_pcr),
            volume_idx=int(args.volume_idx),
            metric_draws=int(args.metric_draws),
        )
        sample_rows.extend(rows)
        summary_rows.append(summary)
        if i % 100 == 0 or i == len(records):
            print(f"[mc] {i}/{len(records)} rollout records -> {len(sample_rows)} samples", flush=True)

    summary_rows = add_conformal_intervals(summary_rows, alpha=alpha)
    samples_df = pd.DataFrame(sample_rows)
    per_patient_df = pd.DataFrame(summary_rows)
    agg = aggregate_summary(per_patient_df)
    calib = calibration_summary(residual_buckets, per_patient_df)
    meta = {
        "model_family": "consistent_graph_scheduled_sampling_rollout",
        "runs_dir": str(args.runs_dir),
        "checkpoint_name": args.checkpoint_name,
        "graphs_root": str(args.graphs_root),
        "n_patients": int(len(patient_folds)),
        "n_rollout_records": int(len(records)),
        "n_mc": int(args.n_mc),
        "metric_draws": int(args.metric_draws),
        "start_visits": [VISITS[i] for i in start_visits],
        "eps_pcr": float(args.eps_pcr),
        "interval": float(args.interval),
        "seed": int(args.seed),
        "device": str(device),
        "leakage_policy": "each patient scored with its held-out fold checkpoint; calibration residuals exclude target patient",
    }
    agg = {"metadata": meta, **agg}
    calib = {"metadata": meta, **calib}

    args.out_dir.mkdir(parents=True, exist_ok=True)
    samples_path = args.out_dir / "conditional_mc_samples.parquet"
    per_patient_path = args.out_dir / "conditional_mc_per_patient.parquet"
    summary_path = args.out_dir / "conditional_mc_summary.json"
    calibration_path = args.out_dir / "conditional_mc_calibration.json"
    samples_df.to_parquet(samples_path, index=False)
    per_patient_df.to_parquet(per_patient_path, index=False)
    summary_path.write_text(json.dumps(agg, indent=2))
    calibration_path.write_text(json.dumps(calib, indent=2))
    print(samples_path, flush=True)
    print(per_patient_path, flush=True)
    print(summary_path, flush=True)
    print(calibration_path, flush=True)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
