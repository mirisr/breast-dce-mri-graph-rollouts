#!/usr/bin/env python3
"""Conditional Monte Carlo for the consistent-graph rollout model.

This script evaluates the retained scheduled-sampling consistent rollout model
under different conditioning start visits:

* T0 observed -> predict T1, T2, T3
* T0,T1 observed -> predict T2, T3
* T0,T1,T2 observed -> predict T3

It uses only the retained scheduled-sampling consistent rollout checkpoints.
For retrospective validation, every patient is evaluated with the checkpoint
from the fold in which that patient was held out.  Monte Carlo samples are
generated by resampling calibrated residuals from other out-of-fold patients
in the same (start_visit, predicted_visit) bucket.
"""

